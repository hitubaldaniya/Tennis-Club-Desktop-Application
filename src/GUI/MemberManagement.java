    private void btnDeleteMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnDeleteMouseReleased
        Member member = getSelectedMember();

        if (member == null) {
            return;
        }
        int dialogResult = JOptionPane.showConfirmDialog(null, "Would you like to delete the member '" + member.getName() + "'?", "Delete", JOptionPane.YES_NO_OPTION);
        if (dialogResult == JOptionPane.YES_OPTION) {
            members.removeMember(member.getId());
            memberList.remove(lstMembers.getSelectedIndex());
            listModel.remove(lstMembers.getSelectedIndex());
        }
    }//GEN-LAST:event_btnDeleteMouseReleased

    private void btnSearchMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSearchMouseReleased
        int memberNumber;

        if (this.txtSearch.getText().isEmpty()) {
            this.txtSearch.setBackground(Color.white);
            showAllMembers();
        } else {
            try {
                memberNumber = Integer.parseInt(this.txtSearch.getText());
                this.txtSearch.setBackground(Color.white);
            } catch (NumberFormatException e) {
                this.txtSearch.setBackground(Color.red);
                return;
            }
            Member searchedMember = members.searchByMemberNumber(memberNumber);
            if (searchedMember == null) {
                JOptionPane.showConfirmDialog(null, "Member not found, displaying all members", "Search", JOptionPane.DEFAULT_OPTION, JOptionPane.INFORMATION_MESSAGE);
                showAllMembers();
            } else {
                listModel.clear();
                listModel.addElement(searchedMember.getMemberNumber());
            }
        }
    }//GEN-LAST:event_btnSearchMouseReleased

    private void lstMembersMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lstMembersMouseClicked
        
        // Double click
        if (evt.getClickCount() == 2) {
            Member member = getSelectedMember();

            if (member == null) {
                return;
            }
            memberForm = new MemberForm(this, members);
            memberForm.viewMember(member);
            memberForm.setVisible(true);
        }
    }//GEN-LAST:event_lstMembersMouseClicked

