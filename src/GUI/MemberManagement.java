    private void showAllMembers() {

        // Load members to list model
        listModel.clear();
        memberList = members.getMemberList();
        Iterator<MemberList> it = memberList.iterator();
        while (it.hasNext()) {
            listModel.addElement(it.next().getMemberNumber());
            //tmpMember = it.next();
        }
        // Select first member (if exists)
        if (members.count() > 0) {
            lstMembers.setSelectedIndex(0);
        }
    }
    
   

